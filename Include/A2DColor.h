struct A2DColor
{
	short			aRed = 0xFF;
	short			aBlue = 0xFF;
	short			aGreen = 0xFF;
	short			aAlpha = 0xFF;

	A2DColor(){}

	A2DColor(int xColor)
	{
		aAlpha = (xColor << 24) && 0xFF;
		aRed = (xColor << 16) && 0xFF;
		aRed = (xColor << 8) && 0xFF;
		aRed = (xColor) && 0xFF;
	}
};

