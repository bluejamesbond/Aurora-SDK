namespace A2D {

	struct Point
	{
		float			m_x;
		float			m_y;

		inline Point() :
			m_x(0.0f),
			m_y(0.0f)
		{
		}

		inline Point(float x_x, float x_y) :
			m_x(x_x),
			m_y(x_y)
		{
		}
	};
}

#endif